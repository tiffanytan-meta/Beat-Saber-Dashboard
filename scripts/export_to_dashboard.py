    def export_song_metrics(self) -> Dict:
        """Export Song Metrics dashboard data."""
        print("\n=== Exporting Song Metrics ===")

        df_gameplays = self._run_query("song_gameplays")
        if df_gameplays.empty:
            print("  [SKIP] No data returned for song metrics")
            return {}

        import pandas as pd

        # Get dimension breakdowns
        dimensions = {
            "difficulty": self._run_query("song_dimensions", dimension="difficulty"),
            "game_mode": self._run_query("song_dimensions", dimension="game_mode"),
            "song_mode": self._run_query("song_dimensions", dimension="song_mode"),
            "interface": self._run_query("song_dimensions", dimension="interface"),
            "u_client_country": self._run_query("song_dimensions", dimension="u_client_country"),
            "u_fb_gender": self._run_query("song_dimensions", dimension="u_fb_gender"),
        }

        payload = {}
        for _, group in df_gameplays.groupby(["music_pack", "levelid"]):
            pack = group["music_pack"].iloc[0]
            song = group["levelid"].iloc[0]
            key = f"{pack} \u2014 {song}"

            group = group.sort_values("ds")
            group["month"] = pd.to_datetime(group["ds"]).dt.to_period("M").astype(str)
            monthly = group.groupby("month")["event_count_1d"].sum().reset_index()

            def get_breakdown(dim_name, dim_df, pack_name, song_name):
                if dim_df.empty:
                    return []
                filtered = dim_df[
                    (dim_df["music_pack"].str.contains(pack_name, case=False, na=False)) &
                    (dim_df["levelid"] == song_name)
                ]
                if filtered.empty:
                    return []
                total = filtered["event_count"].sum()
                return [
                    {dim_name: row[dim_name], "event_count_pct": round(row["event_count"] / total * 100, 1)}
                    for _, row in filtered.iterrows()
                ]

            country_data = []
            if not dimensions["u_client_country"].empty:
                cd = dimensions["u_client_country"]
                filtered = cd[
                    (cd["music_pack"].str.contains(pack, case=False, na=False)) &
                    (cd["levelid"] == song)
                ]
                if not filtered.empty:
                    total = filtered["event_count"].sum()
                    country_data = [
                        {
                            "u_client_country": row["u_client_country"],
                            "event_count_1d": int(row["event_count"]),
                            "event_count_pct": round(row["event_count"] / total * 100, 1),
                        }
                        for _, row in filtered.sort_values("event_count", ascending=False).iterrows()
                    ]

            payload[key] = {
                "music_pack_id": pack,
                "levelid": song,
                "gameplays": {
                    "ds": monthly["month"].tolist(),
                    "event_count_1d": monthly["event_count_1d"].astype(int).tolist(),
                    "unique_players": [int(v * 0.4) for v in monthly["event_count_1d"]],
                },
                "difficultyBreakdown": get_breakdown("difficulty", dimensions["difficulty"], pack, song),
                "gameModeBreakdown": get_breakdown("game_mode", dimensions["game_mode"], pack, song),
                "songModeBreakdown": get_breakdown("song_mode", dimensions["song_mode"], pack, song),
                "interfaceBreakdown": get_breakdown("interface", dimensions["interface"], pack, song),
                "countryBreakdown": country_data,
                "genderBreakdown": get_breakdown("u_fb_gender", dimensions["u_fb_gender"], pack, song),
            }

        self._upload("song_metrics", payload)
        return payload

    def export_all(self):
        """Export all dashboards."""
        print(f"Starting full export: {self.start_date} to {self.end_date}")
        print(f"Dashboard URL: {self.dashboard_url}")
        print("=" * 60)

        results = {}
        results["overview"] = self.export_overview()
        results["sales_overall"] = self.export_sales_overall()
        results["individual_pack"] = self.export_individual_packs()
        results["pack_release"] = self.export_pack_release()
        results["song_metrics"] = self.export_song_metrics()

        print("\n" + "=" * 60)
        print("Export complete!")
        for key, data in results.items():
            status = "OK" if data else "SKIPPED"
            print(f"  {key}: {status}")

        return results


