class BeatSaberExporter:
    """Exports Beat Saber analytics data from Daiquery to the dashboard API."""

    def __init__(
        self,
        api_key: str,
        dashboard_url: str,
        start_date: str = None,
        end_date: str = None,
        namespace: str = "de",
    ):
        self.api_key = api_key
        self.dashboard_url = dashboard_url.rstrip("/")
        self.namespace = namespace

        # Default to last 12 months
        if not end_date:
            end_date = datetime.now().strftime("%Y-%m-%d")
        if not start_date:
            start_date = (datetime.now() - timedelta(days=365)).strftime("%Y-%m-%d")

        self.start_date = start_date
        self.end_date = end_date

        if HAS_DAIQUERY:
            self.client = DaiqueryClient(namespace=namespace)
        else:
            self.client = None

    def _run_query(self, query_key: str, **extra_params) -> "pd.DataFrame":
        """Execute a query and return results as a DataFrame."""
        import pandas as pd

        query = QUERIES[query_key].format(
            start_date=self.start_date,
            end_date=self.end_date,
            quest_app_id=QUEST_APP_ID,
            pc_app_id=PC_APP_ID,
            **extra_params,
        )

        if not self.client:
            print(f"  [MOCK] Would execute query: {query_key}")
            return pd.DataFrame()

        print(f"  Running query: {query_key}...")
        result = self.client.query(query)
        df = result.to_dataframe()
        print(f"  -> Got {len(df)} rows")
        return df

    def _upload(self, dashboard_key: str, payload: Any) -> bool:
        """Upload a payload to the dashboard API."""
        if not HAS_REQUESTS:
            print(f"  [SKIP] Cannot upload {dashboard_key}: requests not installed")
            return False

        url = f"{self.dashboard_url}/api/data/upload"
        data = {
            "apiKey": self.api_key,
            "dashboardKey": dashboard_key,
            "payload": payload,
            "dataDate": self.end_date,
        }

        try:
            resp = requests.post(url, json=data, timeout=60)
            resp.raise_for_status()
            result = resp.json()
            print(f"  [OK] Uploaded {dashboard_key}: {result.get('message', 'success')}")
            return True
        except Exception as e:
            print(f"  [ERR] Failed to upload {dashboard_key}: {e}")
            return False

    # ============================================================
    # Export functions — one per dashboard
    # ============================================================

    def export_overview(self) -> Dict:
        """Export Overview dashboard data."""
        print("\n=== Exporting Overview ===")

        # Active users + stickiness
        df_active = self._run_query("overview_active_users")
        # Gender/Age distribution
        df_demo = self._run_query("overview_gender_age")
        # Attach rate
        df_attach = self._run_query("overview_attach_rate")

        if df_active.empty:
            print("  [SKIP] No data returned for overview")
            return {}

        # Transform active users
        ds_list = df_active["ds"].tolist()
        payload = {
            "activeUsers": {
                "ds": ds_list,
                "dap_cnt": df_active["dap_cnt"].tolist(),
                "wap_cnt": df_active["wap_cnt"].tolist(),
                "map_cnt": df_active["map_cnt"].tolist(),
            },
            "stickiness": {
                "ds": ds_list,
                "daily_stickiness": (df_active["daily_stickiness"] * 100).round(2).tolist(),
                "weekly_stickiness": (df_active["weekly_stickiness"] * 100).round(2).tolist(),
                "monthly_stickiness": (df_active["monthly_stickiness"] * 100).round(2).tolist(),
            },
            "attachRate": {
                "ds": df_attach["ds"].tolist() if not df_attach.empty else ds_list,
                "attach_rate_1d": (df_attach["attach_rate_1d"] * 100).round(2).tolist() if not df_attach.empty else [],
                "attach_rate_lifetime": (df_attach["attach_rate_lifetime"] * 100).round(2).tolist() if not df_attach.empty else [],
            },
            "genderDistribution": [],
            "ageDistribution": [],
            "kpis": {},
        }

        # Gender distribution
        if not df_demo.empty:
            gender_df = df_demo.groupby("u_fb_gender").agg({
                "bs_pct": "sum", "quest_pct": "sum", "delta": "sum"
            }).reset_index()
            payload["genderDistribution"] = [
                {
                    "u_fb_gender": row["u_fb_gender"],
                    "bs_pct": round(row["bs_pct"] * 100, 1),
                    "quest_pct": round(row["quest_pct"] * 100, 1),
                    "delta": round(row["delta"] * 100, 1),
                }
                for _, row in gender_df.iterrows()
            ]

            age_df = df_demo.groupby("u_fb_age_bucket").agg({
                "bs_pct": "sum", "quest_pct": "sum", "delta": "sum"
            }).reset_index()
            payload["ageDistribution"] = [
                {
                    "u_fb_age_bucket": row["u_fb_age_bucket"],
                    "bs_pct": round(row["bs_pct"] * 100, 1),
                    "quest_pct": round(row["quest_pct"] * 100, 1),
                    "delta": round(row["delta"] * 100, 1),
                }
                for _, row in age_df.iterrows()
            ]

        # KPIs from latest row
        latest = df_active.iloc[-1]
        def fmt(n):
            if n >= 1e6: return f"{n/1e6:.1f}M"
            if n >= 1e3: return f"{n/1e3:.1f}K"
            return str(int(n))

        payload["kpis"] = {
            "map_cnt": fmt(latest["map_cnt"]),
            "dap_per_map": f"{(latest.get('dap_per_map', 0) or 0) * 100:.1f}%",
            "daily_stickiness": f"{(latest.get('daily_stickiness', 0) or 0) * 100:.2f}%",
            "attach_rate_lifetime": f"{(df_attach['attach_rate_lifetime'].iloc[-1] * 100):.2f}%" if not df_attach.empty else "N/A",
            "lap_cnt": fmt(latest.get("lap_cnt", 0) or 0),
            "weekly_stickiness": f"{(latest.get('weekly_stickiness', 0) or 0) * 100:.2f}%",
        }

        self._upload("overview", payload)
        return payload

    def export_sales_overall(self) -> Dict:
        """Export Sales Overall dashboard data."""
        print("\n=== Exporting Sales Overall ===")

        df_timeline = self._run_query("sales_timeline")
        df_cumulative = self._run_query("sales_cumulative_by_pack")
        df_attach = self._run_query("sales_attach_rate")
        df_top_songs = self._run_query("sales_top_songs")
        df_new_iap = self._run_query("sales_new_iap_users")

        if df_timeline.empty:
            print("  [SKIP] No data returned for sales overall")
            return {}

        # Monthly aggregation for timeline
        import pandas as pd
        df_timeline["month"] = pd.to_datetime(df_timeline["ds"]).dt.to_period("M").astype(str)
        monthly = df_timeline.groupby("month")["gross_revenue_usd_1d"].sum().reset_index()

        # YoY cumulative
        months_all = monthly["month"].tolist()
        rev_all = monthly["gross_revenue_usd_1d"].tolist()
        mid = len(months_all) // 2
        labels = ["Jan","Feb","Mar","Apr","May","Jun","Jul","Aug","Sep","Oct","Nov","Dec"][:min(12, mid)]

        y1, y2 = [], []
        cum1, cum2 = 0, 0
        for i in range(min(12, mid)):
            cum1 += rev_all[i] / 1e6 if i < len(rev_all) else 0
            y1.append(round(cum1, 1))
            idx2 = i + mid
            cum2 += rev_all[idx2] / 1e6 if idx2 < len(rev_all) else 0
            y2.append(round(cum2, 1))

        # Cumulative by pack (latest date)
        if not df_cumulative.empty:
            latest_date = df_cumulative["ds"].max()
            pack_totals = df_cumulative[df_cumulative["ds"] == latest_date].groupby("music_pack_id")["cumulative_sales"].sum()
            cumulative_by_pack = [
                {"music_pack_id": k, "sales": int(v)}
                for k, v in pack_totals.sort_values(ascending=False).items()
            ]
        else:
            cumulative_by_pack = []

        # Attach rates by pack
        purchase_rate = []
        if not df_attach.empty:
            for _, row in df_attach.iterrows():
                purchase_rate.append({
                    "music_pack_id": row["music_pack_id"],
                    "attach_rate_l1": round((row.get("attach_rate_l1", 0) or 0) * 100, 2),
                    "attach_rate_l7": round((row.get("attach_rate_l7", 0) or 0) * 100, 2),
                    "attach_rate_l28": round((row.get("attach_rate_l28", 0) or 0) * 100, 2),
                    "attach_rate_overall": round((row.get("attach_rate_overall", 0) or 0) * 100, 2),
                })

        # Top songs
        top_songs = []
        if not df_top_songs.empty:
            for _, row in df_top_songs.head(20).iterrows():
                top_songs.append({
                    "levelid": row["levelid"],
                    "music_pack_id": row["music_pack_id"],
                    "gross_revenue_usd_lifetime": int(row["gross_revenue_usd_lifetime"]),
                    "distinct_rev_users_hyperlog_lifetime": int(row["distinct_rev_users_hyperlog_lifetime"]),
                })

        # New IAP users
        new_iap = {"ds": [], "mass_entitled": [], "non_mass_entitled": []}
        if not df_new_iap.empty:
            df_new_iap["month"] = pd.to_datetime(df_new_iap["ds"]).dt.to_period("M").astype(str)
            for month in sorted(df_new_iap["month"].unique()):
                month_data = df_new_iap[df_new_iap["month"] == month]
                new_iap["ds"].append(month)
                me = month_data[month_data["user_type"] == "mass_entitled"]["new_iap_buyers"].sum()
                non_me = month_data[month_data["user_type"] == "non_mass_entitled"]["new_iap_buyers"].sum()
                new_iap["mass_entitled"].append(int(me))
                new_iap["non_mass_entitled"].append(int(non_me))

        total_rev = sum(p["sales"] for p in cumulative_by_pack) if cumulative_by_pack else 0
        total_users = sum(s["distinct_rev_users_hyperlog_lifetime"] for s in top_songs) if top_songs else 1

        def fmt_usd(n):
            if n >= 1e6: return f"${n/1e6:.1f}M"
            if n >= 1e3: return f"${n/1e3:.1f}K"
            return f"${n:,.0f}"

        payload = {
            "salesTimeline": {
                "ds": months_all,
                "gross_revenue_usd_1d": [int(v) for v in rev_all],
            },
            "yoyCumulative": {"labels": labels, "year2024": y1, "year2025": y2},
            "cumulativeByPack": cumulative_by_pack,
            "purchaseRateByPack": purchase_rate,
            "topSongSales": top_songs,
            "newIapUsers": new_iap,
            "kpis": {
                "gross_revenue_usd_lifetime": fmt_usd(total_rev),
                "distinct_rev_users_lifetime": f"{total_users:,}",
                "avg_revenue_per_user": f"${total_rev / max(total_users, 1):.2f}",
                "attach_rate_overall": f"{sum(p.get('attach_rate_overall', 0) for p in purchase_rate) / max(len(purchase_rate), 1):.1f}%",
                "top_music_pack": cumulative_by_pack[0]["music_pack_id"] if cumulative_by_pack else "N/A",
                "wow_growth": "N/A",
            },
        }

        self._upload("sales_overall", payload)
        return payload

    def export_individual_packs(self) -> Dict:
        """Export Individual Pack dashboard data."""
        print("\n=== Exporting Individual Packs ===")

        df_sales = self._run_query("pack_sales")
        df_gameplays = self._run_query("pack_gameplays")
        df_gender = self._run_query("pack_gender")
        df_song_purchases = self._run_query("pack_song_purchases")
        df_sales_windows = self._run_query("pack_sales_windows")
        df_attach = self._run_query("sales_attach_rate")

        if df_sales.empty:
            print("  [SKIP] No data returned for individual packs")
            return {}

        import pandas as pd

        payload = {}
        for pack in MUSIC_PACKS:
            pack_sales = df_sales[df_sales["music_pack_id"] == pack]
            if pack_sales.empty:
                continue

            # Monthly aggregation
            pack_sales = pack_sales.copy()
            pack_sales["month"] = pd.to_datetime(pack_sales["ds"]).dt.to_period("M").astype(str)
            monthly = pack_sales.groupby("month").agg({
                "gross_revenue_usd_1d": "sum",
                "distinct_rev_users_1d": "sum",
            }).reset_index()

            # Gameplay %
            gp = df_gameplays[df_gameplays["music_pack"].str.contains(pack, case=False, na=False)]
            gameplay_pct = round(float(gp["gameplay_pct"].iloc[0]) * 100, 2) if not gp.empty else 0

            # Attach rates
            ar = df_attach[df_attach["music_pack_id"] == pack] if not df_attach.empty else pd.DataFrame()
            attach_rates = {
                "attach_rate_l1": round(float(ar["attach_rate_l1"].iloc[0]) * 100, 2) if not ar.empty else 0,
                "attach_rate_l7": round(float(ar["attach_rate_l7"].iloc[0]) * 100, 2) if not ar.empty else 0,
                "attach_rate_l28": round(float(ar["attach_rate_l28"].iloc[0]) * 100, 2) if not ar.empty else 0,
                "attach_rate_overall": round(float(ar["attach_rate_overall"].iloc[0]) * 100, 2) if not ar.empty else 0,
            }

            # Gender breakdown
            pack_gender = df_gender[df_gender["music_pack"].str.contains(pack, case=False, na=False)]
            gender_breakdown = []
            if not pack_gender.empty:
                total = pack_gender["event_count"].sum()
                for _, row in pack_gender.iterrows():
                    gender_breakdown.append({
                        "u_fb_gender": row["u_fb_gender"],
                        "event_count_pct": round(row["event_count"] / total * 100, 1),
                    })

            # Sales windows
            sw = df_sales_windows[df_sales_windows["music_pack_id"] == pack]
            sales_windows = {
                "sales_7d": int(sw["sales_7d"].iloc[0]) if not sw.empty else 0,
                "sales_28d": int(sw["sales_28d"].iloc[0]) if not sw.empty else 0,
                "sales_overall": int(sw["sales_overall"].iloc[0]) if not sw.empty else 0,
                "bundles_overall": 0,
            }

            release_date = str(sw["release_date"].iloc[0]) if not sw.empty and "release_date" in sw.columns else "N/A"

            # Song purchases
            pack_songs = df_song_purchases[df_song_purchases["music_pack_id"] == pack]
            song_purchases = [
                {
                    "levelid": row["levelid"],
                    "distinct_rev_users": int(row["distinct_rev_users"]),
                    "gross_revenue_usd": int(row["gross_revenue_usd"]),
                }
                for _, row in pack_songs.iterrows()
            ]

            payload[pack] = {
                "release_date": release_date,
                "sales": {
                    "ds": monthly["month"].tolist(),
                    "gross_revenue_usd_1d": monthly["gross_revenue_usd_1d"].astype(int).tolist(),
                    "distinct_rev_users_1d": monthly["distinct_rev_users_1d"].astype(int).tolist(),
                },
                "purchasers": {
                    "ds": monthly["month"].tolist(),
                    "new_iap_buyers": monthly["distinct_rev_users_1d"].astype(int).tolist(),
                    "mass_entitled_buyers": [0] * len(monthly),
                },
                "gameplay_pct": gameplay_pct,
                "attach_rates": attach_rates,
                "genderBreakdown": gender_breakdown,
                "sales_windows": sales_windows,
                "songPurchases": song_purchases,
            }

        self._upload("individual_pack", payload)
        return payload

